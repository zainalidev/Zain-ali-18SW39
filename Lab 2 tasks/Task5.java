class Bitwise{
    public static void main(String[] args) {
    	
    	int number1 = 12, number2 = 25, result;
    	
    	result = number1 | number2;
    	System.out.println(result);
    	result = number1 & number2;
    	System.out.println(result);
    	result = number1 ^ number2;
    	System.out.println(result);
    	result =  ~number2;
    	System.out.println(result);
    	result = ~number1;
    	System.out.println(result);
    	result = 2<<3;
    	System.out.println(result);
    	result = -2>>>5;
    	System.out.println(result);
    	result = 16 >> 2;
    	System.out.println(result);
    }
}







